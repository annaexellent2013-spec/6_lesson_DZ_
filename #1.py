age_groups = {
    "Ліза": "Діти (0-12)",
    "Іван": "Підлітки (13-17)",
    "Марія": "Дорослі (18-59)",
    "Олег": "Літні (60+)"}

name = input("Введіть ваше ім'я: ")

if name in age_groups:
    print(f"{name} належить до групи: {age_groups[name]}.")
else:
    print(f"Ім'я {name} не знайдено в списку користувачів.")